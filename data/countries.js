// Get regions for grouping
const getRegions = () => {
  const regions = [...new Set(countries.map(c => c.region))];
  return regions.sort();
};

// Get countries by region
const getCountriesByRegion = (region) => {
  return countries.filter(c => c.region === region).sort((a, b) => a.name.localeCompare(b.name));
};

// Get country by code
const getCountryByCode = (code) => {
  return countries.find(c => c.code === code);
};

// Search countries
const searchCountries = (query) => {
  const lowerQuery = query.toLowerCase();
  return countries.filter(c => 
    c.name.toLowerCase().includes(lowerQuery) ||
    c.code.toLowerCase().includes(lowerQuery) ||
    c.region.toLowerCase().includes(lowerQuery)
  );
};

// Get all currencies
const getAllCurrencies = () => {
  const uniqueCurrencies = [...new Set(countries.map(c => c.currency))];
  const currencyDetails = uniqueCurrencies.map(curr => ({
    code: curr,
    countries: countries.filter(c => c.currency === curr).map(c => c.code)
  }));
  return currencyDetails.sort((a, b) => a.code.localeCompare(b.code));
};

